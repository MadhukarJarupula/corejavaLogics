class loop_1 
{
	public static void main(String[] args) 
	{
		for (int i = 1;i<=10 ;i++ )
		{
			System.out.println("Good morning  "+i);
		}
		   System.out.println();

      //taking the iteration start = 5 to end 12 by +2
	  for (int i=5;i<=12;i+=2)
	  {
		  System.out.println(i);
	  }
	      System.out.println();


	  //taking the iteration start from 10 and end 30 by +5

	  for (int i=10;i<=30;i+=5)
	  {
		  System.out.println(i);
	  }
	}
}
